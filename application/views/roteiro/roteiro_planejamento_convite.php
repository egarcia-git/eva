<h1>Convites</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação</p>
<h3>Pré-requisitos</h3>
<p>Público-alvo definido e grade de programação</p>
<h3>Objetivo</h3>
<p>Redigir e enviar convites</p>
<h3>Descrição</h3>
<p>Os convites são instrumentos de comunicação dirigida utilizados pela equipe organizadora. Têm como objetivo informar aos públicos os principais dados sobre um evento. Entretanto, o convite se presta a mais do que isso. Pode contribuir para formar opinião e sensibilizar os destinatários para um determinado tema.</p>
<p>Há inúmeras opções de convites, desde as simples e de baixo custo (como mensagens eletrônicas) até as mais elaboradas (como kits contendo, além do convite, brindes e material informativo). A escolha é adaptada ao tipo do evento, ao perfil dos públicos participantes e aos recursos disponíveis. Independentemente do formato escolhido, o zelo no preparo do convite pode revelar muito sobre a organização da iniciativa.</p>
<p>Ao providenciar convites formais, lembre-se de: </p>
<ul>
	<li>Definir qual formato será adotado: convites eletrônicos, impressos ou ambos.</li>
	<li>Redigir textos preferencialmente na ordem direta (sujeito, predicado e complementos) e respeitar as normas cultas da língua portuguesa.</li>
	<li>Apresentar a marca da instituição. A logo é o conjunto do tipo (letras) mais o símbolo.</li> 
	<li>Ao utilizar siglas, desdobrá-las por extenso.</li>
	<li>Usar adjetivos somente quando indispensáveis para complementar a informação.</li>
	<li>Exibir formas de contato para confirmações ou escusas (e-mail, site, telefone, entre outros).</li>
	<li>Definir os trajes exigidos, quando estritamente indispensável.</li> 
</ul>
<p>Antes de expedir os convites, lembre-se de:</p> 
<ul>
	<li>Checar a correção dos dados dos destinatários cadastrados no mailing list.</li>
	<li>Avaliar prazos mínimos de antecedência para o envio, de forma a garantir que o convidado ao receber o convite tenha um tempo para organizar a sua participação.</li>
	<li>Monitorar a expedição e conferir protocolos de entrega.</li>
</ul>
<p>O cuidado visa garantir a entrega dos convites em tempo hábil. O prazo mínimo para a postagem pode ser ampliado em situações de exceção —como em uma eventual interrupção dos serviços dos Correios — mas reduzi-lo ou ignorá-lo pode inviabilizar a participação dos convidados.</p>
