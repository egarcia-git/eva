<h1>Mensagens de agradecimento</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação</p>
<h3>Pré-requisitos</h3>
<p>Lista de apoiadores e patrocinadores</p>
<h3>Objetivo</h3>
<p>Enviar mensagens de agradecimento a apoiadores e patrocinadores</p>
