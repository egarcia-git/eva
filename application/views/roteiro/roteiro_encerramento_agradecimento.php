<p>Ao final do evento, é de bom tom enviar mensagens de agradecimento aos profissionais de apoio, colaboradores, patrocinadores e participantes. É possível inovar incluindo material audiovisual do evento e registros da participação do destinatário, clipping de notícias, links de interesse e relatório com os resultados mais expressivos do trabalho.</p>
