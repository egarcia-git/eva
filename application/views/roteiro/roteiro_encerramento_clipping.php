<h1>Clipping</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação</p>
<h3>Pré-requisitos</h3>
<p>Material audiovisual catalogado</p>
<h3>Objetivo</h3>
<p>Organizar o clipping</p>
<h3>Descrição</h3>
<p>Outra ação de destaque na fase de encerramento é a avaliação da cobertura espontânea das mídias. Uma análise crítica permitirá à equipe coordenadora conhecer o nível de eficácia da estratégia de divulgação adotada.</p>
<p>Os registros das coberturas são parte da documentação do evento e devem ser sistematizados desde as primeiras ações de divulgação, compondo o chamado clipping. Pode ser necessário estender o monitoramento para além do tempo de realização do evento, conforme a repercussão do tema, os resultados do trabalho e o impacto da iniciativa para o público em geral. Muitas vezes é preciso aguardar a publicação de revistas semanais ou mesmo de encartes especiais que têm data específica para serem publicados.</p>
<p>O clipping deve passar por uma análise quantitativa (número de matérias, citações, espaço ocupado etc.) e qualitativa (matérias positivas e negativas, críticas, sugestões, correção das informações etc.). A análise dos conteúdos e os resultados devem constar do relatório final do evento.</p> 
<p>Ao monitorar a cobertura das mídias, considere:</p>
<ul>
	<li>Artigos de jornais</li>
	<li>Citações</li>
	<li>Entrevistas de rádio e televisão</li>
	<li>Registros na internet</li>
	<li>Registros nas redes sociais</li>
	<li>Vídeos e áudios de programas</li>
</ul>
