<h1>Certificados</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação e Recursos Humanos</p>
<h3>Pré-requisitos</h3>
<p>Lista dos artistas participantes, lista de presença dos participantes, lista dos coordenadores e produtores</p>
<h3>Objetivo</h3>
<p>Emitir certificados para artistas e produtores</p>
<h3>Descrição</h3>
<p>É importante para todos os envolvidos no evento que a sua participação seja registrada e documentada. A participação na organização de eventos é bem vista tanto na área acadêmica quanto no mercado de trabalho.</p>
<p>No certificado deve ser especificada a área de atuação do participante e quantas horas foram empenhadas para a realização de suas tarefas.</p>
<h3>Resultado esperado</h3>
<p>Certificados entregues</p>