<h1>Montagem</h1>
<h3>Área responsável</h3>
<p>Coordenação de Produção</p>
<h3>Pré-requisitos</h3>
<p>Infraestrutura e serviços contratados, grade de programação</p>
<h3>Objetivo</h3>
<p>Preparar a montagem do local do evento</p>
<h3>Descrição</h3>
<p>Dias ou mesmo horas antes de o evento começar, é preciso executar a sua montagem, testar equipamentos e deixar toda a infraestrutura pronta e organizada.</p> 
<p>Sugere-se considerar:</p>
<ul>
	<li>dispor estruturas móveis;</li> 
	<li>decorar o ambiente;</li>
	<li>sinalizar acessos;</li>
	<li>checar recursos de iluminação e climatização;</li>
	<li>checar rider técnico, input list e mapa de palco de cada atração, quando houver (exemplo no Anexo 2);</li>
	<li>testar equipamentos de som e de projeção de imagens;</li>
	<li>reservar local para a divulgação da marca dos patrocinadores;</li>
	<li>organizar o local do receptivo;</li>
	<li>organizar o catering dos artistas e convidados;</li>
	<li>organizar o camarim;</li>
	<li>atualizar roteiros;</li>
	<li>reservar o local destinado à imprensa e separar os press kits que serão entregues aos profissionais credenciados;</li>
	<li>reunir equipes para treinamentos e ensaios e</li>
	<li>reforçar a limpeza ao final da montagem do evento.</li>
</ul>
<p>A antecedência necessária para dar início à montagem deve ser avaliada pela equipe organizadora, conforme a complexidade do evento e o número de profissionais e recursos envolvidos.</p>
<h3>Resultado esperado</h3>
<p>Infraestrutura pronta para receber o evento</p>