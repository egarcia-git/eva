<h1>Classificação e arquivamento da documentação</h1>
<h3>Área responsável</h3>
<p>Coordenação Administrativa e Financeira</p>
<h3>Pré-requisitos</h3>
<p>Documentos administrativos gerais</p> 
<h3>Objetivo</h3>
<p>Organizar e arquivar documentos</p>
<h3>Descrição</h3>
<p>A documentação administrativa do evento deve ser classificada e arquivada. Ofícios, notas fiscais, correspondências, registros de confirmação e escusas, formulários de avaliação dos participantes, relatórios e demais documentos de referência compõem a memória do evento e podem ser importantes referências para iniciativas futuras.</p>
<h3>Resultado esperado</h3>
<p>Arquivos classificados, documentados e arquivados</p>