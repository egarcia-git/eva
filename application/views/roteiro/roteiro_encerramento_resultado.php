<h1>Divulgação dos resultados</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação</p>
<h3>Pré-requisitos</h3>
<p>Relatório final do evento</p>
<h3>Objetivo</h3>
<p>Divulgar os resultados obtidos com o evento</p>
<h3>Descrição</h3>
<p>Ao final de todos os esforços, sugere-se a elaboração de um texto resumido com os principais resultados do evento. O documento se diferencia do relatório final porque se presta especificamente a divulgar os ganhos do trabalho. Sugere-se que seja compartilhado com as diversas áreas envolvidas no planejamento, na organização e na execução do evento, uma vez que nem todas são destinatárias do relatório final.</p> 
<p>Resultados de interesse público devem ser divulgados aos meios e profissionais de comunicação envolvidos na cobertura ou que ainda estejam abertos a registrar a iniciativa.</p> 
<p>Ao elaborar o documento, considere:</p> 
<ul>
	<li>avanços, novas descobertas, ganhos;</li>
	<li>quadros comparativos com edições anteriores ou eventos similares;</li> 
	<li>avaliação dos participantes;</li> 
	<li>previsão de novas iniciativas.</li>
</ul>
<h3>Resultado esperado</h3>
<p>Resultados divulgados</p>
