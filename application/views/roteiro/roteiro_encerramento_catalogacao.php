<h1>Catalogação e arquivamento dos registros audiovisuais</h1>
<h3>Área responsável</h3>
<p>Coordenação de Comunicação</p>
<h3>Pré-requisitos</h3>
<p>Material audiovisual do evento</p>
<h3>Objetivo</h3>
<p>Organizar e arquivar registros audiovisuais</p>
<h3>Descrição</h3>
<p>O mesmo tratamento dado à documentação administrativa deve ser dispensado aos registros de áudio e de imagens gerados ao longo do processo de iniciação, planejamento, execução e também no encerramento. O material deve ser catalogado e arquivado de forma a compor a memória do evento e servir de referência para iniciativas futuras.</p>
<h3>Resultado esperado</h3>
<p>Material catalogado e arquivado</p>