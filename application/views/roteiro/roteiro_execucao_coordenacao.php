<h1>Tarefas da coordenação</h1>
<h3>Área responsável</h3>
<p>Equipe de coordenação</p>
<h3>Pré-requisitos</h3>
<p>Equipe de coordenação definida, grade de programação</p>
<h3>Objetivo</h3>
<p>Executar tarefas da coordenação</p>
<h3>Descrição</h3>
<p>Cabe ao coordenador ou à equipe coordenadora durante a condução do evento:</p>
<ul>
	<li>ter e delegar autonomia para tomar decisões e promover ajustes no que foi planejado;</li>
	<li>coordenar a equipe nos postos de trabalho;</li>
	<li>fazer contatos, solicitar informações e esclarecer dúvidas;</li>
	<li>monitorar o receptivo dos artistas, convidados e patrocinadores;</li>
	<li>monitorar, com o apoio da equipe de Assessoria de Imprensa, o atendimento aos profissionais de comunicação que fazem a cobertura do evento;</li>
	<li>monitorar a condução das etapas previstas no roteiro;</li>
	<li>estar disponível e atento durante toda a execução do evento, supervisionando os trabalhos.</li>
</ul>
<h3>Resultado esperado</h3>
<p>Tarefas da coordenação executadas</p>