<h1>Questionário de avaliação e feedback</h1>
<h3>Área responsável</h3>
<p>Coordenação Geral</p>
<h3>Pré-requisitos</h3>
<p>Presença das equipes de coordenação e produção</p>
<h3>Objetivo</h3>
<p>Obter avaliação da equipe de coordenação e produção</p>
<h3>Descrição</h3>
<p>A avaliação consiste no registro e na sistematização das opiniões sobre o evento.</p> 
<p>Os instrumentos utilizados para a sondagem podem variar. Entre outros, destacam-se os questionários, a coleta de dados, pesquisas e reuniões de feedback. O desafio é conhecer a opinião dos participantes, organizadores, apoiadores e de quaisquer grupos envolvidos diretamente no evento. Um exemplo de formulário de pesquisa pode ser conferido no Anexo 3.</p>
<p>Dados tangíveis devem ser quantificados em tabelas, gráficos e em outros formatos de fácil visualização, mas, em se tratando de eventos, muitos dos resultados obtidos nem sempre são passíveis de quantificação. Nesses casos, o registro das avaliações deve ser consolidado em documentos narrativos ou descritivos. O fundamental é que os dados e informações levantadas não se percam e sejam compartilhados no relatório final do evento para servirem de subsídios no futuro. Tal cuidado é ainda mais essencial em eventos com periodicidade regular.</p>
<p>Algumas perguntas podem auxiliar nessa análise:</p>
<ul>
	<li>O tipo do evento, o formato, o local, a data e o horário foram adequados?</li>
	<li>Houve participação do público convidado?</li>
	<li>O público foi mobilizado ou sensibilizado para o tema do evento?</li>
	<li>O evento gerou a repercussão pretendida?</li>
	<li>A abordagem do evento foi adequada?</li>
	<li>Houve imprevistos? O que podia ter sido feito de uma forma diferente?</li>
	<li>Investimento: analisando os recursos empregados e o alcance dos objetivos, o saldo foi positivo?</li>
</ul>
<p>A avaliação interna da equipe podem incluir esclarecimento de papéis e responsabilidades, feedback construtivo para os membros da equipe, descoberta de questões desconhecidas ou não resolvidas, desenvolvimento de planos de treinamento individuais, desempenho em relação ao cumprimento das tarefas, desempenho do cumprimento do cronograma e do orçamento e o estabelecimento de metas específicas para períodos futuros.</p>
