<h1>Relatório final</h1>
<h3>Área responsável</h3>
<p>Coordenação Geral</p>
<h3>Pré-requisitos</h3>
<p>Documento de avaliação do evento, documentação catalogada</p> 
<h3>Objetivo</h3>
<p>Elaborar relatório final</p>
<h3>Descrição</h3>
<p>O relatório final é o documento que reúne a análise crítica de todas as fases do evento, dificuldades e soluções encontradas durante a condução dos trabalhos e, em especial, resultados e sugestões de melhorias.</p>
<p>O texto pode ser coloquial, respeitadas as normas cultas da língua portuguesa, e deve prezar pela clareza e simplicidade.</p> 
<p>Ao redigir o relatório:</p>
<ul>
	<li>escreva preferencialmente na ordem direta (sujeito, predicado e complementos).</li>
	<li>fuja de imprecisões, repetições desnecessárias e estrangeirismos.</li>
	<li>seja econômico em adjetivos. Use-os somente quando indispensáveis para completar a informação.</li>
	<li>apresente análises quantitativas e qualitativas do objeto do relatório.</li>
	<li>reúna dados históricos do objeto, estatísticas, gráficos, fotos, entre outros recursos para situar o leitor.</li>
</ul>
<p>Os relatórios podem ser sucintos ou mais completos e detalhados. A opção por um ou outro modelo deve ser proporcional ao grau de complexidade do evento.</p> 
<p>Ao elaborar relatórios completos e detalhados, considere: </p>
<ul>
	<li>adequação do dia, horário e local escolhido para o evento;</li>
	<li>adequação da programação;</li>
	<li>número e perfil dos participantes (artistas, expositores, convidados, autoridades etc.), origem, nacionalidade, domicílio, sexo, idade, profissão;</li>
	<li>eficiência das estratégias de comunicação adotadas;</li>
	<li>atuação dos palestrantes, moderadores, apresentadores, entre outros;</li>
	<li>adequação do perfil dos coordenadores e compromisso dos produtores e prestadores de serviço;</li>
	<li>eficiência dos procedimentos administrativos adotados;</li>
	<li>qualidade dos serviços prestados;</li>
	<li>ajuste dos recursos e receitas às demandas do evento;</li> 
	<li>objetivos atendidos;</li>
	<li>documentação do evento: registros fotográficos, vídeos, notícias divulgadas, resultados das pesquisas de opinião;</li>
	<li>dificuldades encontradas durante as fases de planejamento, organização e condução do evento e decisões que se mostraram eficientes;</li>
	<li>quantidade de mídia espontânea gerada;</li>
	<li>contribuições para servir de insumos às futuras iniciativas.</li>
</ul>
<h3>Resultado esperado</h3>
<p>Relatório final</p>