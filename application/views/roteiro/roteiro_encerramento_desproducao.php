<h1>Desprodução</h1>
<h3>Área responsável</h3>
<p>Coordenação de Produção</p>
<h3>Pré-requisitos</h3>
<p>Evento finalizado</p>
<h3>Objetivo</h3>
<p>Desmontar a estrutura do evento</p>
<h3>Descrição</h3>
<p>Os cuidados com a infraestrutura no período do pós-evento incluem desmontagem das instalações, devolução de mobiliário, equipamentos e itens de ambientação aos setores de origem, transporte de materiais, reorganização do espaço conforme a configuração original, limpeza e vistoria para averiguar a ocorrência de algum dano material e localizar objetos perdidos.</p>
<h3>Resultado esperado</h3>
<p>Estrutura de realização do evento entregue da mesma maneira que foi recebida</p>