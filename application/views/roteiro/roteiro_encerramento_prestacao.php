<h1>Prestação de contas</h1>
<h3>Área responsável</h3>
<p>Coordenação Administrativa e Financeira</p>
<h3>Pré-requisitos</h3>
<p>Notas fiscais e compras, pagamentos efetuados e serviços prestados</p>
<h3>Objetivo</h3>
<p>Prestar contas do financeiro</p>
<h3>Descrição</h3>
<p>Essa é uma ação indispensável. O procedimento de atesto das notas fiscais e de avaliação dos serviços prestados é conduzido e supervisionado pela equipe coordenadora. O resultado deve constar do relatório final do evento, com registros do fluxo de entrada e saída de recursos. Notas fiscais de compras realizadas durante o evento devem ser informadas aqui, para que os custos sejam reembolsados.</p>
<p>Eventuais falhas ou descumprimentos na prestação de serviços precisam ser relatados para que se observem os procedimentos e as penalidades previstas em contrato. Além disso, servirão para orientar futuras contratações.</p>
<h3>Resultado esperado</h3>
<p>Balanço geral do evento</p>