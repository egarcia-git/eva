<h1>Suporte aos artistas e equipe organizadora</h1>
<h3>Área responsável</h3>
<p>Coordenação de Produção e Receptivo</p>
<h3>Pré-requisitos</h3>
<p>Equipe de coordenação e produção, recursos materiais e financeiros disponíveis, artistas participantes e grade de programação</p>
<h3>Objetivo</h3>
<p>Viabilizar a participação dos artistas e da equipe organizadora do evento</p>
<h3>Descrição</h3>
<p>Cada evento é único, ainda que haja modelos e orientações gerais para servir de referência. O cenário ideal é aquele em que a equipe trabalha coesa e pautada pelo interesse de superar as expectativas dos participantes. Serviços de suporte planejados especialmente para atendê-los são essenciais nesse contexto.</p>
<p>Ao organizar um evento, considere:</p>
<ul>
<li>a necessidade de providenciar traslados, hospedagem e alimentação dos artistas ou de quaisquer participantes que integram a programação do evento;</li>
<ul>
<li>Para eventos de grande porte, é preferível fazer um mapa de traslados, hospedagens e alimentação, conforme os Apêndices E e F;</li>
<li>Uma alternativa para a acomodação dos artistas é a hospedagem solidária. Residências próximas ao local de realização do evento podem se candidatar a receber os artistas e hospedá-los durante os dias em que forem participar do evento. Pode-se tanto fazer contato direto com as residências ou abrir um sistema de inscrição on-line, conforme o Apêndice G.</li>
<li>A alimentação das equipes e artistas deve ser controlada através de listas ou com tickets de alimentação, para que fique dentro do orçamento estipulado.</li>
</ul>
<li>a possibilidade de firmar parcerias e credenciar empresas para oferecer condições facilitadas e preços competitivos para o transporte, alimentação e hospedagem dos convidados;</li>
<li>a necessidade de providenciar serviços de tradução simultânea ou consecutiva;</li>
<li>a possibilidade de dispor de serviço médico de apoio durante todo o tempo em que durar o evento;</li>
<li>a conveniência de elaborar uma lista com sugestões de restaurantes, centros culturais e de lazer, serviços médicos de emergência e outros serviços na cidade-sede do evento.</li>
</ul>
<h3>Resultado esperado</h3>
<p>Mapa de traslados e hospedagens, parcerias fechadas e lista de sugestões</p>
