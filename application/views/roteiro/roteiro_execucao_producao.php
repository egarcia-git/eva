<h1>Tarefas da produção</h1>
<h3>Área responsável</h3>
<p>Equipe de produção</p>
<h3>Pré-requisitos</h3>
<p>Equipe de produção definida, grade de programação</p>
<h3>Objetivo</h3>
<p>Executar tarefas da produção</p>
<h3>Descrição</h3>
<p>Cabe à equipe de produção durante a condução do evento: </p>
<ul>
	<li>estar em seus postos de trabalho com antecedência suficiente para checar procedimentos e organizar materiais. Conforme o número de pessoas envolvidas e a abrangência do evento, convém destacar um coordenador para cada área;</li>
	<li>submeter a novos testes e checagens equipamentos de som e de projeção de imagens;</li>
	<li>reforçar a limpeza dos ambientes;</li>
	<li>registrar e confirmar dados dos convidados para a elaboração de certificados;</li>
	<li>recepcionar, dar boas vindas, prestar orientações sobre o evento, encaminhar, acomodar e esclarecer eventuais dúvidas dos convidados sobre a programação do evento;</li>
	<li>orientar os artistas que se apresentarão;</li>
	<li>estar disponível para esclarecer dúvidas, organizar entrevistas e auxiliar os profissionais de imprensa no que for preciso;</li>
	<li>encerrar o evento, agradecer presenças e conduzir os convidados ao local de saída.</li>
</ul>
<h3>Resultado esperado</h3>
<p>Tarefas da produção executadas</p>
