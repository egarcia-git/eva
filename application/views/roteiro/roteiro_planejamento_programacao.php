<h1>Programação das atrações</h1>
<h3>Área responsável</h3>
<p>Coordenação de Programação e Recursos Humanos</p>
<h3>Pré-requisitos</h3>
<p>Artistas que participarão do evento</p>
<h3>Objetivo</h3>
<p>Definir a programação das atrações</p>
<h3>Descrição</h3>
<p>A programação é o conjunto de atividades voltadas aos participantes no dia do evento, desde a abertura até o encerramento dos trabalhos. Uma forma usual e eficiente de estruturá-la é por horários, momento a momento, conforme o exemplo a seguir:</p> 
<p></p>
<div style="padding: 10px; border: 1px solid grey;">
<strong>PROGRAMAÇÃO</strong>	
<br>Primeiro dia
<br><strong>18h</strong> Abertura do evento
<br><strong>19h</strong> Atração 1 
<br><strong>20h</strong> Atração 2 
<p></p>
Segundo dia
<br><strong>16h</strong> Atração 3
<br><strong>17h30</strong> Atração 4
<br><strong>19h</strong> Atração 5
<br><strong>22h</strong> Encerramento do evento
</div>
<p></p>
<p>Parece óbvio, mas não é exagero reiterar a importância de o programa ser viável na prática e de fato refletir os objetivos planejados. Múltiplas atividades, às vezes até concorrentes, tempos de duração subestimados, intervalos exíguos não respeitados e indisponibilidade de agenda dos participantes estão entre os exemplos mais comuns de erros. Não raro os organizadores desconsideram esses cuidados básicos, gerando frustração e comprometendo a qualidade dos trabalhos.</p>
<p>Cabe aqui aos Recursos Humanos alocar os produtores para auxiliarem em cada uma das atrações, garantindo que sempre haja pelo menos um em cada atração, afim de solucionar qualquer problema que possa ocorrer e acompanhar o artista desde as preparações de sua apresentação até o encerramento. O Apêndice H traz um exemplo de como os coordenadores e produtores podem ser organizados.</p>
<h3>Resultado esperado</h3>
<p>Grade de programação do evento</p>
